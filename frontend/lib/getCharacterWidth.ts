export default function getCharacterWidth(text: string, font: string): number {
  let canvas = document.createElement('canvas');
  let ctx = canvas.getContext('2d');
  if (!ctx) {
    return 0;
  } else {
    ctx.font = font;
    return ctx.measureText(text).width
  }
}